valor_um = int(input("Informe o primeiro valor inteiro: "))
valor_dois = int(input("Informe o segundo valor inteiro: "))
valor_tres = int(input("Informe o valor real: "))

resposta_A = (valor_um * 2) * (valor_dois / 2)
resposta_B = (3 * valor_um) + valor_tres
resposta_C = valor_tres * 3

print(f"O produto do primeiro com a metade do segundo é: {resposta_A}")
print(f"O triplo do primeiro com o terceiro: {resposta_B}")
print(f"O triplo do valor real é: {resposta_C}")