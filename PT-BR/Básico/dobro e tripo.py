valor_um = int(input("Informe o primeiro valor inteiro: "))
valor_dois = int(input("Informe o segundo valor inteiro: "))
valor_tres = float(input("Informe o valor real: "))

print(f"O produto do primeiro com a metade do segundo é: {(valor_um * 2) * (valor_dois / 2)}")
print(f"O triplo do primeiro com o terceiro: {(3 * valor_um) + valor_tres}")
print(f"O triplo do valor real é: {valor_tres * 3}")